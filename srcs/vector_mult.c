#include <rtv1.h>

t_vector	vector_mult(t_vector v1, t_vector v2)
{
	t_vector	ret;

	ret.x = v1.x * v2.x;
	ret.y = v1.y * v2.y;
	ret.z = v1.z * v2.z;
	return (ret);
}
