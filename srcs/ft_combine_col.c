#include <rt.h>

t_col	ft_combine_col(t_col col1, t_col col2)
{
	t_col	tmp;

	tmp.r = (col1.r + col2.r);
	tmp.g = (col1.g + col2.g);
	tmp.b = (col1.b + col2.b);
	return (tmp);
}
