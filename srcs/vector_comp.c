#include <rtv1.h>

int		vector_comp(t_vector v1, t_vector v2)
{
	if (v1.x != v2.x)
		return (0);
	if (v1.y != v2.y)
		return (0);
	if (v1.z != v2.z)
		return (0);
	exit(1);
	return (1);
}


